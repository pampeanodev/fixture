// src/data/thirdPlaceMapping.ts
//
// Assigns the 8 best third-placed teams to their Round-of-32 matches using
// FIFA's OFFICIAL lookup table (World Cup 2026 Regulations, Annexe C). FIFA does
// NOT use an algorithm here: Annexe C precomputes all 495 combinations of which
// 8 of the 12 third-placed groups qualify, and fixes one specific assignment per
// combination. A greedy/constraint solver produces a *valid* assignment but
// almost never FIFA's chosen one (it matched only 14 of 495 cases), which is why
// the bracket disagreed with the officially confirmed thirds.
//
// Source: FIFA World Cup 2026 Regulations, Annexe C, as transcribed in
// https://en.wikipedia.org/wiki/Template:2026_FIFA_World_Cup_third-place_table

