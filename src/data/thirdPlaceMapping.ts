export function assignThirdPlaceSlots(qualifyingGroups: string[]): ThirdPlaceAssignment {
  // Annexe C only defines the full 8-group case. While the group stage is still
  // in progress the caller passes an incomplete set (or none) — there is no
  // official assignment yet, so return empty and let the bracket stay projected.
  if (qualifyingGroups.length !== 8) return {};

  const key = [...qualifyingGroups].sort().join("");
  const encoded = OFFICIAL_THIRD_PLACE_TABLE[key];
  if (!encoded) return {};

  const assignment: ThirdPlaceAssignment = {};
  for (let i = 0; i < SLOT_ORDER.length; i++) {
    assignment[encoded[i]] = SLOT_ORDER[i];
  }
  return assignment;
}
